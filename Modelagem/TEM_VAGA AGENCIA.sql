CREATE DATABASE TEMVAGA;
USE TEMVAGA;

CREATE TABLE PASSAGEM (
PROMOÇÃO DECIMAL(5,2),
CODIGO_PASSAGEM INTEGER PRIMARY KEY auto_increment,
VALOR_VIAGEM decimal(9,6),
CODIGO_DESTINO INTEGER,
