);

CREATE TABLE CLIENTE (
NOME VARCHAR(60),
IDADE VARCHAR(6),
CPF VARCHAR(20) PRIMARY KEY,
TELEFONE VARCHAR(10),
EMAIL VARCHAR(50),
DATA_NASCIMENTO datetime(6),
ENDEREÇO VARCHAR(50),
CEP VARCHAR(20),
BAIRRO VARCHAR(50),
NUMERO VARCHAR(10),
PAIS VARCHAR(50),
ESTADO VARCHAR(50)
);
CREATE TABLE DESTINO (
CODIGO_DESTINO INTEGER PRIMARY KEY auto_increment,
ESTADO VARCHAR(100),
CIDADE VARCHAR(100)
);

